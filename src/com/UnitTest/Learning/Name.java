package com.UnitTest.Learning;

public class Name {


  public String firstName(String fName){
    return fName;
  }

  public String middleName(String mName) {
    return mName;
  }

  public String lastName(String lName) {
    return lName;
  }

  public String fullName(String fName, String mName, String lName) {
    return fName + " " +  mName + " " + lName;
  }
}
